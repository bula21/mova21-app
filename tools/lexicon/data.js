var lang = urlParams.get('lang');

if (!lang) {
	lang = 'de';
}

let descriptions = {
	"de": "Hier findet ihr einige nützliche Begriffe und Redewendungen, die euch die Kommunikation mit anderen Teilnehmenden erleichtern werden! Viel Spass!",
	"fr": "Voici quelques termes et phrases utiles qui t’aideront à communiquer plus facilement avec les autres participant-e-s ! Amuse-toi bien !",
	"it": "Ecco alcuni termini e frasi utili che ti aiuteranno a comunicare più facilmente con altri/e partecipanti! Buon divertimento!"
}

let desc = document.querySelector('#description');
desc.innerHTML = descriptions[lang];

let element = document.querySelector('#dAccordion');

categories.forEach(function (cat, index) {
	var title = cat.title[lang];
	if (title) {
		element.innerHTML = element.innerHTML + `<h2 class="lexicon-section">${title}</h2>`;
	}
	cat.items.forEach(function(item, index2) {
		var id = 'item' + index + '_' + index2;
		var html = `<div class="accordion-item">
			<h2 class="accordion-header" id="heading${id}">
				<button class="accordion-button" type="button" data-bs-toggle="collapse" data-bs-target="#collapse${id}" aria-expanded="false" aria-controls="collapse${id}">
					${item[lang]}
				</button>
			</h2>
			<div id="collapse${id}" class="accordion-collapse collapse" aria-labelledby="heading${id}" data-bs-parent="#dAccordion">
				<div class="accordion-body">
					<table>
						<tr ${lang === 'de' ? 'style="display: none"' : ''}>
							<td class="lexicon-lang">DE</td>
							<td class="lexicon-text">${item.de}</td>
						</tr>
						<tr ${lang === 'fr' ? 'style="display: none"' : ''}>
							<td class="lexicon-lang">FR</td>
							<td class="lexicon-text">${item.fr}</td>
						</tr>
						<tr ${lang === 'it' ? 'style="display: none"' : ''}>
							<td class="lexicon-lang">IT</td>
							<td class="lexicon-text">${item.it}</td>
						</tr>
					</table>
				</div>
			</div>
		</div>`;
		element.innerHTML = element.innerHTML + html;
	})
})
